module.exports ={

    /**
     * Find And Print distinct triples which sum = 0.
     * @param {num, num} Numbers - it take array as integer.
     * return distance.
     */
    
    distance:function(x2,y2){
        return Math.sqrt(Math.pow(x2,2)+Math.pow(y2,2));
    }
}